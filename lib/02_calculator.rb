def add (n1, n2)
	n = n1 + n2
	return n
end

def subtract (n1, n2)
	n = n1 - n2
	return n
end

def sum (arr)
	sum_final = arr.sum
	return sum_final
end

def multiply (n1, n2)
	n = n1 * n2
	return n
end

def power (n1, n2)
	n = n1 ** n2
	return n
end

def factorial (n1)
	n = Math.gamma(n1 + 1)
	return n
end